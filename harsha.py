import pywhatkit as kit
from datetime import datetime

def send_message(phone_no):
    now = datetime.now()
    hour = now.hour
    minute = now.minute + 1  # Add 1 minute to the current minute to schedule the message in the next minute
    kit.sendwhatmsg(phone_no, "", hour, minute)

